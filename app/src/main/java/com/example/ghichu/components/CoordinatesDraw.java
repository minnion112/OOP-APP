package com.example.ghichu.components;

public class CoordinatesDraw {
    private float x,y;
    public CoordinatesDraw(){

    }
    public CoordinatesDraw(float x, float y){
        this.x=x;
        this.y=y;
    }

    public float getX(){
        return this.x;
    }

    public float getY() {
        return y;
    }
}
